    private void sign_up_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sign_up_buttonActionPerformed
        PreparedStatement ps;
        ResultSet rs;

        String query = "INSERT INTO `Users` (`user_name`, `user_email`, `user_password`) VALUES (?,?,?)";
        try {
            if (validateData()) {
                ps = JDBC.getConnection().prepareStatement(query);

                ps.setString(1, user_name);
                ps.setString(2, email);
                ps.setString(3, hashedPassword);
                int action = ps.executeUpdate();
                if (action != 0) {
                    JOptionPane.showMessageDialog(null, "Registration completed.", "Success", 2);
                    login_page lp = new login_page();
                    lp.setVisible(true);
                    lp.setLocationRelativeTo(null);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "ERROR");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(register_page.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_sign_up_buttonActionPerformed

