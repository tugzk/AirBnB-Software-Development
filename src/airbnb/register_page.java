    public boolean validateData() {
        if (username_rg_tx.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter your Username");
            username_rg_tx.requestFocusInWindow();
            return false;
        } 
        if (email_rg_tx.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter your Email");
            email_rg_tx.requestFocusInWindow();
            return false;
        } 
        if(String.valueOf(pass_rg_pf.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null, "Please enter your Password");
            email_rg_tx.requestFocusInWindow();
            return false;
        }
        if(String.valueOf(conf_pass_rg_pf.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null, "Please confirm your Password");
            email_rg_tx.requestFocusInWindow();
            return false;
        }
        if(!String.valueOf(pass_rg_pf.getPassword()).equals(String.valueOf(conf_pass_rg_pf.getPassword()))){
            JOptionPane.showMessageDialog(null, "Password doesn't match");
            email_rg_tx.requestFocusInWindow();
            return false;
        }
        else {
            user_name = username_rg_tx.getText();
            String password = String.valueOf(pass_rg_pf.getPassword());
            email = email_rg_tx.getText();
            hashedPassword = BCrypt.hashpw(password, BCrypt.gensalt());
            return true;
        }
    }

